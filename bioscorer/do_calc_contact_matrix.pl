# Global variables
my @Atoms;				# Surface atom groups from one chain
my @Crosses;			# Cross positions of triangles from the other chain
my @Matrix;				# The output matrix
my $Num_Con = 0;		# The number of contacts
my $Num_Checks = 0;		# Number of distance checks
my @Thresholds;			# The contact thresholds (look out for dist/2)
my @Triangle_Indices; 	# The huge hash matrix for triangle => triangle type

# Get user input
my $atom_file = $ARGV[0]; # File with surface atom groups from one chain
open(ATOM, $atom_file) or die "Can't read $atom_file: $!\n";
@Atoms = <ATOM>;
chomp @Atoms;
close ATOM;

my $cross_file = $ARGV[1]; # File with cross positions of triangles from the other chain
open(CROSS, $cross_file) or die "Can't read $cross_file: $!\n";
@Crosses = <CROSS>;
chomp @Crosses;
close CROSS;

# LOG START OF PROCESS
my $log_start = `echo "[\$(date +"%F %H:%M:%S")] $0: $atom_file $cross_file"`;
print STDERR $log_start;

# The contact thresholds
@Thresholds = &load_thresholds();

# The huge hash matrix for triangle => triangle type 
@Triangle_Indices = &load_t_matrix();

# LOG &load_t_matrix()
print STDERR "\t\t&load_t_matrix() completed\n";

# The output matrix, initialize
Atoms:
	for (my $i=1; $i<=17; $i++) {
		Triangles:
			for (my $j=1; $j<=969; $j++) {
				$Matrix[$i][$j] = 0;
			}
	}

# Coordinates for centers of ideal probe positions
my @coor_c = ();
# Coordinates for centers of atoms from the other chain
my @coor_a = ();

# Internal numbering of ideal probe positions or triangles
my $t_pos = -1;
Crosses:
	for (my $i = 0; $i < scalar(@Crosses); $i++) {

		# "fdc    33.889765   2.445434  39.563038     1    5    2"
		my ($code, $c_x, $c_y, $c_z, @other) = split(/\s+/, $Crosses[$i]);

		# Quick and Dirty: skip all unknown triangle types
		next Crosses unless ($code =~ /[a-m][a-m][a-m]/);

		$t_pos++;

		my ($p1, $p2, $p3) = split('', $code);
	
		my $c_p1 = &single_trans($p1);
		my $c_p2 = &single_trans($p2);
		my $c_p3 = &single_trans($p3);

		my $t_type = $Triangle_Indices[$c_p1][$c_p2][$c_p3];

		$coor_c[$t_pos][0] = $t_type;
		$coor_c[$t_pos][1] = $c_x;
		$coor_c[$t_pos][2] = $c_y;
		$coor_c[$t_pos][3] = $c_z;

	} # Crosses

# Internal numbering of atoms from the other chain
my $a_pos = -1;
Atoms:
	for (my $i = 0; $i < scalar(@Atoms); $i++) {
		
		my $a = $Atoms[$i];
			
		# "  6  31.235000   3.992000  38.915000     1"
		$a =~ s/^\s+//;
		my ($num, $a_x, $a_y, $a_z, @other2) = split(/\s+/, $a);

		# Quick and Dirty: skip all unknown atom group types
		next unless ( exists($Thresholds[$num]) );

		$a_pos++;

		$coor_a[$a_pos][0] = $num;
		$coor_a[$a_pos][1] = $a_x;
		$coor_a[$a_pos][2] = $a_y;
		$coor_a[$a_pos][3] = $a_z;

	} # Atoms


# Detect triangle to atom contacts
Crosses:
	for (my $i = 0; $i < scalar(@coor_c); $i++) {
		$Num_Checks += scalar @coor_a;

		Atoms:
			for (my $j = 0; $j < scalar(@coor_a); $j++) {
				# The distance calculated in $dist is the distance between
				# the center of the ideal probe location and the center
				# of the atom from the other chain. This distance will be
				# in the range from 1.42*2 until 1.88*2.

				my $T_c = 2*$Thresholds[$coor_a[$j][0]];
				my $dist = ($coor_a[$j][1] - $coor_c[$i][1])*($coor_a[$j][1] - $coor_c[$i][1])
							+ ($coor_a[$j][2] - $coor_c[$i][2])*($coor_a[$j][2] - $coor_c[$i][2])
							+ ($coor_a[$j][3] - $coor_c[$i][3])*($coor_a[$j][3] - $coor_c[$i][3]);
				if ($dist <= $T_c*$T_c) {
					$Num_Con++;
					$Matrix[$coor_a[$j][0]][$coor_c[$i][0]]++;
				}
			} # Atoms
	} # Crosses

# LOG Detect triangle to atom contacts completed
print STDERR "\t\tChecked: $Num_Checks, found: $Num_Con\n";

# The output matrix, print to STDOUT
	Triangles:
		for (my $j=1; $j<=969; $j++) {
			printf ("T%3d", $j);
			Atoms:
				for (my $i=1; $i<=17; $i++) {
	    			printf "%11d", $Matrix[$i][$j];
				}
			print "\n";
		}

# End the script, define the subs
exit;

# load the threshold for the 13 atom types
sub load_thresholds {
	
	my @threshold = ();
	
	$threshold[1]=1.61;  # C3H0
    $threshold[2]=1.76;  # C3H1
    $threshold[3]=1.88;  # C4H1
    $threshold[4]=1.88;  # C4H2
    $threshold[5]=1.88;  # C4H3
    $threshold[6]=1.64;  # N3H0
    $threshold[7]=1.64;  # N3H1
    $threshold[8]=1.64;  # N3H2
    $threshold[9]=1.64;  # N4H3
    $threshold[10]=1.42; # O1H0
    $threshold[11]=1.46; # O2H1
    $threshold[12]=1.77; # S2H0
    $threshold[13]=1.77; # S2H1
	$threshold[14]=1.88; # BBCA C4H1
    $threshold[15]=1.61; # BBCO C3H0
    $threshold[16]=1.64; # BBNH N3H1
    $threshold[17]=1.42; # BBOX O1H0
	return @threshold;
}

# calculate the distance by using 6 coordinates
sub distance{
	my @data=@_;
	my $x=$data[0]-$data[3];
	my $y=$data[1]-$data[4];
	my $z=$data[2]-$data[5];
	my $sum=sqrt($x*$x+$y*$y+$z*$z);
}

# translate a single letter into numbers
sub single_trans{
	my $letter=$_[0];
	if($letter eq "a"){return '0';}
	if($letter eq "b"){return '1';}
	if($letter eq "c"){return '2';}
	if($letter eq "d"){return '3';}
	if($letter eq "e"){return '4';}
	if($letter eq "f"){return '5';}
	if($letter eq "g"){return '6';}
	if($letter eq "h"){return '7';}
	if($letter eq "i"){return '8';}
	if($letter eq "j"){return '9';}
	if($letter eq "k"){return '10';}
	if($letter eq "l"){return '11';}
	if($letter eq "m"){return '12';}
	if($letter eq "n"){return '13';}
	if($letter eq "o"){return '14';}
	if($letter eq "p"){return '15';}
	if($letter eq "q"){return '16';}
}

